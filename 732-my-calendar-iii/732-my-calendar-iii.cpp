class MyCalendarThree {
public:
    map<int,int>mp;
    MyCalendarThree() {
        
    }
    
    int book(int start, int end) {
        mp[start]++;
        mp[end]--;
        int mx=0,s=0;
        for(auto &i:mp)
        {
            s+=i.second;
            mx=max(mx,s);
        }return mx;
    }
};

